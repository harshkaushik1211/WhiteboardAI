class AvatarResult(BaseModel):
    """Project-level avatar clip metadata (single video for the whole project).

    P1: SadTalker generates ONE avatar.webm per project (driven by combined.wav).
    The renderer uses scene start-frame offsets to show the correct window of
    the single clip for each scene — no per-scene clips needed.
    """
    avatar_schema_version: int = 1          # Refinement A
    # Relative path to the transparent WebM inside the project directory.
    clip_path: str                          # e.g. "avatars/avatar.webm"
    total_duration: float                   # probed duration of the video clip
    audio_duration: float = 0.0             # probed duration of combined.wav (P2 validation)
    duration_valid: bool = True             # False if drift > tolerance (P2)
    duration_drift: float = 0.0            # abs(audio - video) seconds
    provider: str = "sadtalker"
    provider_version: str = "sadtalker_v1"  # P4 versioning
    manifest_version: int = 1               # P4 manifest version
    cache_key: Optional[str] = None         # P6 SHA256 cache key
    layout: str = "pip"                     # P10 layout mode
    
    # New diagnostic/observability fields (Refinement C)
    background_removed: bool = True
    cache_hit: bool = False
    generation_time_seconds: Optional[float] = None
    validation_passed: bool = True


# Backward-compatibility alias — existing code that imports AvatarSceneResult will still work.
AvatarSceneResult = AvatarResult


