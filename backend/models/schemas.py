from enum import Enum
from typing import Any, Dict, List, Optional
from pydantic import BaseModel, Field, field_validator


class PipelineStep(str, Enum):
    PENDING = "pending"
    SCRIPT = "script"
    SCENES = "scenes"
    SVG = "svg"
    VOICE = "voice"
    VOICE_EXPORTED = "voice_exported"     # F5-TTS: narration pack exported, awaiting audio
    VOICE_IMPORTED = "voice_imported"     # F5-TTS: audio imported, ready to render
    AVATAR = "avatar"
    AVATAR_EXPORTED = "avatar_exported"   # SadTalker: package exported, awaiting clips
    AVATAR_IMPORTED = "avatar_imported"   # SadTalker: clips imported, ready to render
    TIMELINE = "timeline"
    RENDER = "render"
    COMPLETE = "complete"
    ERROR = "error"


