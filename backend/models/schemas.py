class SceneSchema(BaseModel):
    scene_id: int
    narration: str
    visual_description: str
    keywords: List[str] = Field(default_factory=list)
    duration: float = 8.0
    # Educational scene role — used by visual planner for intent-aware layout
    # HOOK | INTUITION | EXPLANATION | EXAMPLE | VISUALIZATION | COMPARISON |
    # CAUSE_EFFECT | FORMULA | SUMMARY | REAL_WORLD_APPLICATION
    scene_type: Optional[str] = None
    # Natural bridging phrase spoken at the end to connect to the next scene
    transition_phrase: Optional[str] = None


class LessonPlan(BaseModel):
    """Lightweight pre-script educational structure generated by the lesson planner.

    Guides script generation with learning objectives, optimal scene sequence,
    concept complexity estimate, and curated real-world example suggestions.
    All fields are optional so that fallback defaults work gracefully.
    """
    learning_objectives: List[str] = Field(default_factory=list)
    # Ordered list of scene_type values defining the teaching arc
    scene_sequence: List[str] = Field(default_factory=list)
    # Complexity drives scene count: simple=5-6, medium=7-9, complex=10-12
    concept_complexity: str = "medium"
    estimated_scene_count: int = 6
    recommended_examples: List[str] = Field(default_factory=list)
    domain: str = ""
    # Optional quality feedback injected on rewrite passes
    quality_feedback: List[str] = Field(default_factory=list)
    # Phase 3 additions:
    common_misconceptions: List[str] = Field(default_factory=list)
    prerequisites: List[str] = Field(default_factory=list)
    attention_profile: str = "high_school"


