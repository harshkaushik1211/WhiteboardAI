class SceneElement(BaseModel):
    id: str
    type: str  # svg, image, arrow, text, label, highlight
    concept: Optional[str] = None
    asset_id: Optional[str] = None
    shape: Optional[str] = None  # deprecated for educational visuals
    position: Optional[Position] = None
    size: Optional[Size] = None
    from_point: Optional[Point] = Field(None, alias="from")
    to_point: Optional[Point] = Field(None, alias="to")
    animation: str = "stroke_reveal"
    delay: float = 0.0
    duration: float = 2.0
    label: Optional[str] = None
    text: Optional[str] = None
    color: str = "#1a1a2e"
    svg_path: Optional[str] = None
    image_path: Optional[str] = None
    stroke_data_path: Optional[str] = None
    asset_library_path: Optional[str] = None
    action: Optional[str] = None
    animation_intent: Optional[str] = None
    motion_profile: Optional[str] = None
    target_id: Optional[str] = None

    @field_validator("from_point", "to_point", mode="before")
    @classmethod
    def _coerce_point(cls, v):
        if v is None:
            return v
        if isinstance(v, Point):
            return v
        if isinstance(v, Position):
            return Point(x=v.x, y=v.y)
        if isinstance(v, dict):
            return Point.model_validate(v)
        return v

    class Config:
        populate_by_name = True


